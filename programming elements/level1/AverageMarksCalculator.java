/*
 * This program calculates the average
 * using basic arithmetic operations
 */
public class AverageMarksCalculator {

    public static void main(String[] args) {

        // Perform addition and division to find the average
        int math = 94;
        int physics = 95;
        int chemistry = 96;

        // Calculate the average value
        double avg = (math + physics + chemistry) / 3.0;

        // Display the calculated average
        System.out.println("Sam's average mark in PCM is " + avg);
    }
}
